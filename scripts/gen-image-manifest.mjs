// Generates lib/images.ts from the actual processed files in public/images/**.
// Run after enhance-images.mjs so room/gallery galleries reflect whatever count
// of photos each category ended up with.
import { readdirSync, existsSync, writeFileSync } from "node:fs";
import { join, resolve } from "node:path";

const ROOT = resolve(import.meta.dirname, "..");
const IMG = join(ROOT, "public", "images");
const ls = (p) => (existsSync(p) ? readdirSync(p).filter((f) => f.endsWith(".webp")).sort() : []);

const ROOMS = [
  { slug: "deluxe-room", name: "Deluxe Room" },
  { slug: "super-deluxe-room", name: "Super Deluxe Room" },
  { slug: "triple-deluxe-room", name: "Triple Deluxe Room" },
  { slug: "family-four-bed-room", name: "Family Four Bed Room" },
];
const ROOM_DIM = { w: 1500, h: 1000 };
const GAL_DIM = { w: 1500, h: 1000 };

function roomImgs(slug, name) {
  const files = ls(join(IMG, "rooms", slug));
  return files.map((f, i) => ({
    src: `/images/rooms/${slug}/${f}`,
    alt: `${name} at Hotel Siddhi Vinayak, Jodhpur${i ? ` — view ${i + 1}` : ""}`,
    width: ROOM_DIM.w, height: ROOM_DIM.h,
  }));
}
const galImgs = (sub, label) => ls(join(IMG, "gallery", sub)).map((f, i) => ({
  src: `/images/gallery/${sub}/${f}`, alt: `${label} at Hotel Siddhi Vinayak, Jodhpur${i ? ` ${i + 1}` : ""}`,
  width: GAL_DIM.w, height: GAL_DIM.h,
}));

const roomImages = Object.fromEntries(ROOMS.map((r) => [r.slug, roomImgs(r.slug, r.name)]));
const property = galImgs("property", "Hotel Siddhi Vinayak");
const dining = galImgs("dining", "Restaurant");
const heroExterior = "/images/hero/hotel-siddhi-vinayak-exterior-jodhpur.webp";
const galleryRooms = ROOMS.flatMap((r) => roomImages[r.slug].slice(0, 2));

const j = (o) => JSON.stringify(o, null, 2);
const out = `/**
 * PRODUCTION IMAGE MANIFEST — auto-generated by scripts/gen-image-manifest.mjs
 * from the processed WebP in public/images/**. Do not edit by hand; re-run the
 * generator after enhance-images.mjs. next/image serves responsive variants.
 */
export type Img = { src: string; alt: string; width: number; height: number };

export const ogImage = "/og-image.jpg";

export const hero = {
  home: { src: "${heroExterior}", alt: "Hotel Siddhi Vinayak exterior in Jodhpur, Rajasthan", width: 1920, height: 1080 },
  rooms: ${j(roomImages["super-deluxe-room"][0] || { src: heroExterior, alt: "Hotel Siddhi Vinayak", width: 1500, height: 1000 })},
  gallery: ${j(property[0] || { src: heroExterior, alt: "Hotel Siddhi Vinayak", width: 1500, height: 1000 })},
  about: ${j(property[1] || property[0] || { src: heroExterior, alt: "Hotel Siddhi Vinayak", width: 1500, height: 1000 })},
  restaurant: ${j(dining[0] || { src: heroExterior, alt: "Restaurant", width: 1500, height: 1000 })},
} satisfies Record<string, Img>;

export const roomImages: Record<string, Img[]> = ${j(roomImages)};

export type GalleryCat = "Rooms" | "Property" | "Dining";
export const galleryByCategory: Record<GalleryCat, Img[]> = ${j({ Rooms: galleryRooms, Property: property, Dining: dining })};
`;
writeFileSync(join(ROOT, "lib", "images.ts"), out);
const counts = ROOMS.map((r) => `${r.slug}=${roomImages[r.slug].length}`).join(" ");
console.log(`lib/images.ts generated. Rooms: ${counts} | property=${property.length} dining=${dining.length}`);
